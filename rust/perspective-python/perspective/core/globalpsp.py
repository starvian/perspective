from typing import Awaitable, Callable
import perspective

_psp_server = perspective.PySyncServer()


class Session:
    def __init__(self, callback: Callable[[bytes], Awaitable[None]]):
        self._session = _psp_server.new_session(callback)

    async def handle_request(self, msg: bytes):
        self._session.handle_request(msg)

    def poll(self):
        self._session.poll()
