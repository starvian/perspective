const { test, expect } = require("@playwright/test");
const perspective = require("@finos/perspective");
const { test_arrow } = require("./test_arrows");

test("Can get hosted table name from worker", async () => {
    const worker = perspective.worker();

    const testTableName1 = Math.random().toString();
    const _table = worker.table(test_arrow, { name: testTableName1 });
    const names = await worker.get_hosted_table_names();

    expect(names).toContain(testTableName1);

    const testTableName2 = Math.random().toString();
    const _table2 = worker.table(test_arrow, { name: testTableName2 });
    const names2 = await worker.get_hosted_table_names();

    expect(names2).toContain(testTableName1);
    expect(names2).toContain(testTableName2);
});
