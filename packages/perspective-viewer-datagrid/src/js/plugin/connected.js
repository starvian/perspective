/**
 * Custom Element lifecycle method.
 */
export function connectedCallback() {
    if (!this._toolbar) {
        this._toolbar = document.createElement(
            "perspective-viewer-datagrid-toolbar"
        );
    }

    this.parentElement.appendChild(this._toolbar);
}
